# SOMA
n1 = int(input('Digite um valor: '))
n2 = int(input('Outro: '))
s = n1 + n2
print('Como os valores foram typados como inteiros: A soma entre {} e {} vale {}' .format(n1, n2, s))

# CONCATENAÇÃO
